public class OrscaBeanAdmin {

    String installedDevices;
    String companyName;
    String totalDevices;
    String availabledevices;
    String tripcount;

    public String getInstalledDevices() {
        return installedDevices;
    }

    public void setInstalledDevices(String installedDevices) {
        this.installedDevices = installedDevices;
    }

    public String getCompanyName() {
        return companyName;
    }

    public void setCompanyName(String companyName) {
        this.companyName = companyName;
    }

    public String getTotalDevices() {
        return totalDevices;
    }

    public void setTotalDevices(String totalDevices) {
        this.totalDevices = totalDevices;
    }

    public String getAvailabledevices() {
        return availabledevices;
    }

    public void setAvailabledevices(String availabledevices) {
        this.availabledevices = availabledevices;
    }

    public String getTripcount() {
        return tripcount;
    }

    public void setTripcount(String tripcount) {
        this.tripcount = tripcount;
    }
}
